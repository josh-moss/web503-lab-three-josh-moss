function setup() {
    createCanvas(710, 400);
// createCanvas() allows us to create a canvas at a specific size.
    background(255);
// background() gives the canvas a background colour in this case 255 is white.
    stroke(255, 0, 0);
// stroke allows us to add a stroke and set its colour. In this case it is red.
    strokeWeight(6);
// strokeWeight() edits the thickness of the stroke. 
}

function draw() {
// function draw allows us to create our shapes.
    background(255);
// background() allows us to set the shapes background. In this case it is white.
    rect(30, 20, 200, 100, 20);
// rectagnle params, used to create a rounded edge rectangle.
}